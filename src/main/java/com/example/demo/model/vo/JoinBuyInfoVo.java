package com.example.demo.model.vo;

public class JoinBuyInfoVo {
	
	private int totalPieceNum;
	private int myPieceNum;
	private int leastNum;
	private String title;
	private String content;
	private String limitPlayer;
	private Byte planType;
	
	public int getTotalPieceNum() {
		return totalPieceNum;
	}
	public void setTotalPieceNum(int totalPieceNum) {
		this.totalPieceNum = totalPieceNum;
	}
	public int getMyPieceNum() {
		return myPieceNum;
	}
	public void setMyPieceNum(int myPieceNum) {
		this.myPieceNum = myPieceNum;
	}
	public int getLeastNum() {
		return leastNum;
	}
	public void setLeastNum(int leastNum) {
		this.leastNum = leastNum;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getLimitPlayer() {
		return limitPlayer;
	}
	public void setLimitPlayer(String limitPlayer) {
		this.limitPlayer = limitPlayer;
	}
	public Byte getPlanType() {
		return planType;
	}
	public void setPlanType(Byte planType) {
		this.planType = planType;
	}
	
	

}
